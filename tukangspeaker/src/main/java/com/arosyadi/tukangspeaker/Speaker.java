package com.arosyadi.tukangspeaker;

public class Speaker {
    String kelengkapan;
    String ballroom;
    String pemesan;

    public String getKelengkapan() {
        return kelengkapan;
    }

    public void setKelengkapan(String kelengkapan) {
        this.kelengkapan = kelengkapan;
    }

    public String getBallroom() {
        return ballroom;
    }

    public void setBallroom(String ballroom) {
        this.ballroom = ballroom;
    }

    public String getPemesan() {
        return pemesan;
    }

    public void setPemesan(String pemesan) {
        this.pemesan = pemesan;
    }
}
