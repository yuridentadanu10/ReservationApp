package android.example.com.reservationapp.recyclersound;

public class Speaker {
    String nama ;
    int rating;


    public String getNama() {
        return nama;
    }

    public void setNama(String nama) {
        this.nama = nama;
    }

    public int getRating() {
        return rating;
    }

    public void setRating(int rating) {
        this.rating = rating;
    }
}
